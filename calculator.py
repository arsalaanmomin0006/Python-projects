F_n = int(input("Enter first no:"))
S_n = int(input("Enter second no:"))
operation = input("Enter what operation to do(+, -, *, /):")
if operation == "+":
    print(F_n + S_n)
elif operation == "-":
    print(F_n - S_n)
elif operation == "*":
    print(F_n * S_n)
elif operation == "/":
    print(F_n / S_n)
